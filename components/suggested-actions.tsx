'use client';

import { motion } from 'framer-motion';
import { Button } from './ui/button';
import { memo } from 'react';
import type { UseChatHelpers } from '@ai-sdk/react';

interface SuggestedActionsProps {
  chatId: string;
  append: UseChatHelpers['append'];
}

function PureSuggestedActions({ chatId, append }: SuggestedActionsProps) {
  const suggestedActions = [
    {
      title: 'Access control system',
      label: 'for 11 employees.',
      action:
        'I want to do a payroll for 11 employees with password based access control.',
    },
    {
      title: 'JWT Authentication API',
      label: 'with login and token refresh.',
      action:
        'Build a RESTful API that supports JWT-based login and token refresh functionality.',
    },
    {
      title: 'Image processing API',
      label: 'for resizing and format conversion.',
      action:
        'Build a Python-based FastAPI service that allows users to upload an image and get it back resized or converted to another format like WebP or PNG.',
    },
    {
      title: 'Log monitoring script',
      label: 'for suspicious SSH login attempts.',
      action:
        'Write a Python script that monitors auth.log and flags multiple failed SSH login attempts from the same IP.',
    },
  ];

  return (
    <div
      data-testid="suggested-actions"
      className="grid sm:grid-cols-2 gap-2 w-full"
    >
      {suggestedActions.map((suggestedAction, index) => (
        <motion.div
          initial={{ opacity: 0, y: 20 }}
          animate={{ opacity: 1, y: 0 }}
          exit={{ opacity: 0, y: 20 }}
          transition={{ delay: 0.05 * index }}
          key={`suggested-action-${suggestedAction.title}-${index}`}
          className={index > 1 ? 'hidden sm:block' : 'block'}
        >
          <Button
            variant="ghost"
            onClick={async () => {
              window.history.replaceState({}, '', `/chat/${chatId}`);

              append({
                role: 'user',
                content: suggestedAction.action,
              });
            }}
            className="text-left border rounded-xl px-4 py-3.5 text-sm flex-1 gap-1 sm:flex-col w-full h-auto justify-start items-start"
          >
            <span className="font-medium">{suggestedAction.title}</span>
            <span className="text-muted-foreground">
              {suggestedAction.label}
            </span>
          </Button>
        </motion.div>
      ))}
    </div>
  );
}

export const SuggestedActions = memo(PureSuggestedActions, () => true);
